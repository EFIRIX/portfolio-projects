def get_text(language: str, key: str, **kwargs) -> str:
    lang = language if language in translations else DEFAULT_LANGUAGE
    template = translations.get(lang, {}).get(key, translations[DEFAULT_LANGUAGE].get(key, key))
    if kwargs:
        try:
            return str(template).format(**kwargs)
        except Exception:
            return str(template)
    return str(template)


def get_translator(language: str) -> Callable[[str], str]:
    def _translate(key: str, **kwargs) -> str:
        return get_text(language, key, **kwargs)

    return _translate
