/**
 * platforms.config.ts — БИЗНЕС-ПРАВИЛА площадок, а не часть текста промпта.
 *
 * Почему лимиты живут здесь, а не в промпте:
 *  1. Лимиты Wildberries и Ozon — это внешнее ограничение платформы, а не
 *     "пожелание к стилю". Их нельзя доверять модели: LLM не обязана считать
 *     символы точно и регулярно ошибается на 5-20%.
 *  2. Промпт можно нарушить — код нарушить нельзя. Мы валидируем длину
 *     результата ПОСЛЕ генерации (см. lib/generation.ts) против этих значений.
 *     Если превышено — повторный запрос с явным требованием сократить.
 *  3. Лимиты меняются со стороны площадок. Менять надо одну константу здесь,
 *     а не искать число внутри строки промпта в трёх местах.
 *
 * Значения ниже — фактические лимиты на момент написания (2024–2025).
 * Их легко проверить и обновить, потому что они собраны в одном месте.
 */

export type PlatformId = "wildberries" | "ozon";

export interface FieldLimit {
  /** Человекочитаемое имя поля в карточке площадки. */
  label: string;
  /** Жёсткий максимум символов, который принимает площадка. */
  maxChars: number;
  /**
   * Целевой максимум для генерации (с запасом от жёсткого лимита).
   * LLM просим уложиться сюда, чтобы даже при небольшом перелёте
   * остаться в пределах maxChars.
   */
  targetChars: number;
  /** Подсказка модели о структуре/содержании поля. */
  guidance: string;
}

export interface PlatformConfig {
  id: PlatformId;
  name: string;
  /** Заголовок / наименование товара. */
  title: FieldLimit;
  /** Описание / аннотация товара. */
  description: FieldLimit;
  /** Особенности площадки, влияющие на тон и структуру текста. */
  styleNotes: string;
}

