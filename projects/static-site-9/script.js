// ====== ЛОГИКА ======
function shuffle(arr) {
  return arr.sort(() => Math.random() - 0.5);
}

let cards = shuffle([...ALL_CARDS]);
let i = 0, correct = 0, wrong = 0;
let errors = [];
const startTime = Date.now();

const qEl = document.getElementById("question");
const aEl = document.getElementById("answer");
const rEl = document.getElementById("result");
const pEl = document.getElementById("progress");
const summaryEl = document.getElementById("summary");
const errorsBtn = document.getElementById("errorsMode");

document.getElementById("themeToggle").onclick = () => {
  document.body.classList.toggle("dark");
  document.body.classList.toggle("light");
};

function render() {
  if (i >= cards.length) return finish();
  qEl.textContent = cards[i].q;
  aEl.value = "";
  rEl.textContent = "";
  pEl.textContent = `${i + 1} / ${cards.length}`;
  aEl.focus();
}

function finish() {
  document.getElementById("card").classList.add("hidden");

  const total = correct + wrong;
  const percent = total ? Math.round((correct / total) * 100) : 0;
  const t = Math.floor((Date.now() - startTime) / 1000);

  summaryEl.innerHTML = `
    <h2>Результат</h2>
    ✔ Правильных: ${correct}<br>
    ❌ Неправильных: ${wrong}<br>
    📊 Процент правильных: ${percent}%<br>
    ⏱ Время: ${Math.floor(t/3600)}ч ${Math.floor(t%3600/60)}м ${t%60}с
  `;
  summaryEl.classList.remove("hidden");

  if (errors.length) errorsBtn.classList.remove("hidden");
}

errorsBtn.onclick = () => {
  cards = shuffle([...errors]);
  i = correct = wrong = 0;
  errors = [];
  summaryEl.classList.add("hidden");
  document.getElementById("card").classList.remove("hidden");
  errorsBtn.classList.add("hidden");
  render();
};

aEl.addEventListener("keydown", e => {
  if (e.key === "Enter") {
    if (aEl.value.trim().toLowerCase() === cards[i].m) {
      correct++;
      i++;
      render();
    } else {
      wrong++;
      errors.push(cards[i]);
      rEl.textContent = `❌ Неверно → ${cards[i].a}`;
    }
  }
});

render();